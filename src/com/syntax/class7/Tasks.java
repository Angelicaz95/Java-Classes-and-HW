package com.syntax.class7;

public class Tasks {

	public static void main(String[] args) {
		// print numbers from 1 to 100 in one line with space
		for( int i=1; i<=100; i++) {
			System.out.print(i+" ");
		}
		
		System.out.println("-----print numbers from 100 to 1 ----- ");
		for( int a=100; a>=1; a--) {
			System.out.print(a+" ");
		}
	}

}
